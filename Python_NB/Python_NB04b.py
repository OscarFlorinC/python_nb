# Cadenas de Texto
cadenaTexto = 'Esto es una cadena de texto'
print(cadenaTexto)

# Listas, es una colección de datos
listaEjemplo = [3, "lista", 3, 4.5]
print(listaEjemplo)

# Tuplas, es una colección de datos pero inmutable
tuplaEjemplo1 = (4,6,19)
tuplaEjemplo2 = ('x',1,'Carrillo' )
print(tuplaEjemplo1 + tuplaEjemplo2)

# Diccionarios, es una colección desordenada de pares clave-valor
diccionarioEjemplo={
    'Nombre': 'Juan',
    'Apellido': 'Bose',
    'Correo': 'jb@mail.com'
}
print(diccionarioEjemplo)

# Set, es una colección desordenada de valores únicos
setEjemplo = {6,6,45,'a','b','c','c'}
print(setEjemplo)